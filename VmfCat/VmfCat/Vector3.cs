namespace VmfCat
{
   public struct Vector3
   {
      public int X
      {
         get;
      }

      public int Y
      {
         get;
      }

      public int Z
      {
         get;
      }

      public Vector3( int x, int y, int z )
      {
         X = x;
         Y = y;
         Z = z;
      }
   }
}
